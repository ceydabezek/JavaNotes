public class Loops {
    public static void main(String[] args) {
        //FOR
        for (int i=1;i<=10;i++ //i+=2 (odd number)
        ){
            System.out.println(i);
        }
        System.out.println("For Loop over");

        //WHÄ°LE 
        int i=1;
        while (i<10){
            System.out.println("i");
        }
        System.out.println("While Loop over");

    }
}
